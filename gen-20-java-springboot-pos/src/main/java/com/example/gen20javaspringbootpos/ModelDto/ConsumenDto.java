package com.example.gen20javaspringbootpos.ModelDto;

public class ConsumenDto {
    private Long id;
    private String name;

    public ConsumenDto(Long id, String name) {
        this.id = id;
        this.name = name;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
